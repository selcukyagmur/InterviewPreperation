package top50JournalDev;

public class Q2SwapTwoNumbers {

    public static void main(String[] args) {
        //How to swap two numbers without using a third variable?

        int a=2;
        int b=3;

        a=a+b; //a becomes 5
        b=a-b; //b becomes 2
        a=a-b; //a becomes 3

        System.out.println("a = " + a+ ", "+ "b = " + b);
    }



        
}
